IF OBJECT_ID(N'dbo.Employee', N'U') IS NULL
BEGIN

CREATE TABLE Employee (
	ID INT Identity PRIMARY KEY , 
	Name NVARCHAR(100),
	IDepartment INT,
	CreatedAt DATETIME DEFAULT GETDATE()
);
END

GO

IF OBJECT_ID(N'dbo.Department', N'U') IS NULL
BEGIN

CREATE TABLE Department (
	ID INT PRIMARY KEY,
	Name NVARCHAR(100),
	CreatedAt DATETIME DEFAULT GETDATE()
);


END




